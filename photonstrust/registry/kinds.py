"""Kind registry (v0.1).

This module defines the backend-owned "component kind" catalog used by:
- the web editor trust panel (units, defaults, ranges),
- optional validation surfaces, and
- documentation generation (future).

The registry is deliberately lightweight: it is a descriptive contract and does
not execute physics.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any


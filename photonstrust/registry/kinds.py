REGISTRY_SCHEMA_VERSION = "0.1"


@dataclass(frozen=True)
class _Missing:
    pass


_MISSING = _Missing()


def _param(
    name: str,
    typ: str,
    *,
    unit: str | None = None,
    required: bool = False,
    default: Any = _MISSING,
    minimum: float | None = None,
    maximum: float | None = None,
    enum: list[Any] | None = None,
    description: str = "",
    applies_when: dict[str, Any] | None = None,
) -> dict[str, Any]:
    out: dict[str, Any] = {
        "name": str(name),
        "type": str(typ),
        "required": bool(required),
        "description": str(description or ""),
    }
    if unit is not None:
        out["unit"] = str(unit)
    if default is not _MISSING:
        out["default"] = default
    if minimum is not None:
        out["min"] = float(minimum)
    if maximum is not None:
        out["max"] = float(maximum)
    if enum is not None:
        out["enum"] = list(enum)
    if applies_when is not None:
        out["applies_when"] = dict(applies_when)
    return out


def _kind(
    kind: str,
    *,
    title: str,
    category: str,
    in_ports: list[str],
    out_ports: list[str],
    in_port_domains: dict[str, str] | None = None,
    out_port_domains: dict[str, str] | None = None,
    params: list[dict[str, Any]],
    api_enabled: bool = True,
    cli_enabled: bool = True,
    notes: list[str] | None = None,
) -> dict[str, Any]:
    in_port_list = [str(p) for p in in_ports]
    out_port_list = [str(p) for p in out_ports]

    default_domain = "optical" if str(category).strip().lower() == "pic" else "control"
    in_domains = {port: str(default_domain) for port in in_port_list}
    out_domains = {port: str(default_domain) for port in out_port_list}
    if isinstance(in_port_domains, dict):
        for k, v in in_port_domains.items():
            in_domains[str(k)] = str(v)
    if isinstance(out_port_domains, dict):
        for k, v in out_port_domains.items():
            out_domains[str(k)] = str(v)

    out: dict[str, Any] = {
        "kind": str(kind),
        "title": str(title),
        "category": str(category),
        "ports": {"in": in_port_list, "out": out_port_list},
        "port_domains": {"in": in_domains, "out": out_domains},
        "params": list(params),
        "availability": {
            "api_enabled": bool(api_enabled),
            "cli_enabled": bool(cli_enabled),
        },
    }
    if notes:
        out["notes"] = [str(n) for n in notes]
    return out


